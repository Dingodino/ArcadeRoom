#include "tick.h"


//====================================================================================
// Constructors
//====================================================================================

Tick::Tick()
{
    setParent(0);
    moveToThread(this);
}


//====================================================================================
// Private Operations
//====================================================================================

void Tick::run()
{
    while(true)
    {
        msleep(50);
        emit ticked();
    }
}
